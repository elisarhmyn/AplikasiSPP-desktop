    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow();
        if (i == -1) {
            return;
        }
        String id_petugas = (String) tm.getValueAt(i, 0);
        idpetugas=id_petugas;
        
        String username = (String) tm.getValueAt(i, 1);
        txtusernm.setText(username);
        
        String password = (String) tm.getValueAt(i, 2);
        txtpass.setText(password);
        
        String nama_petugas = (String) tm.getValueAt(i, 3);
        txtnmpetugas.setText(nama_petugas);
        
        String level = (String) tm.getValueAt (i, 4);
        cblevel.setSelectedItem(level);
    }//GEN-LAST:event_jTable1MouseClicked

