    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int i = jTable1.getSelectedRow();
        if (i == -1) {
            return;
        }
        String id_kelas = (String) tm.getValueAt(i, 0);
        idkelas=id_kelas;

        String nama_kelas = (String) tm.getValueAt(i, 1);
        txtnmkls.setText(nama_kelas);

        String kompetensi_keahlian = (String) tm.getValueAt (i, 2);
        cbkomkel.setSelectedItem(kompetensi_keahlian);

    }//GEN-LAST:event_jTable1MouseClicked

