    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (txtusernm.getText().equals("")|| txtpass.getText().equals("")||txtnmpetugas.getText().equals("")||cblevel.getSelectedItem().equals(""))
        {
            JOptionPane.showMessageDialog(this,"Data harus terisi semua");
        } else {
         try {
            PreparedStatement ps = con.prepareStatement("INSERT INTO petugas VALUES (?,?,?,?,?)");
            ps.setInt(1, 0);
            ps.setString(2, txtusernm.getText());
            ps.setString(3, txtpass.getText());
            ps.setString(4, txtnmpetugas.getText());
            ps.setString(5, cblevel.getSelectedItem().toString());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this,"Data telah tersimpan");
            refreshTable();
            
            txtusernm.setText("");
            txtpass.setText("");
            txtnmpetugas.setText("");
            cblevel.setSelectedItem("");
            
        } catch (Exception e) {
            System.out.print("ERROR KUERI KE DATABASE:\n" + e + "n\n");
        }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

